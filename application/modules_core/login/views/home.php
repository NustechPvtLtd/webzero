//doesn't block the load event
    function createIframe() {
        var i = document.createElement("iframe");
        i.src = "https://www.youtube.com/embed/5FRPKMHFPW4";
        i.scrolling = "auto";
        i.frameborder = "0";
        i.width = "200px";
        i.height = "100px";
        i.class = "embed-responsive-item";
        document.getElementById("load-youtube").appendChild(i);
    }
    ;

// Check for browser support of event handling capability
    if (window.addEventListener)
        window.addEventListener("load", createIframe, false);
    else if (window.attachEvent)
        window.attachEvent("onload", createIframe);
    else
        window.onload = createIframe;

    $("#video_play").on('hidden.bs.modal', function(e) {

        $("#video_play iframe").attr("src", $("#video_play iframe").attr("src"));
    });

    $("#main_navbar").click(function() {
        $("#video_play").modal('hide');
    });

</script>